package com.bifi.feelicity.MapTools;

public class Attachment {
	public String path;
	public String type;
	
	public Attachment(String path, String type) {
        this.path = path;
        this.type = type;
   }
	
	public String getPath() {
		return path;
	}
	
	public String getType() {
		return type;
	}

}
